package bean;

public class PlayerInfo {
	private Integer player_id;
	private String player_name;
	private String player_country;
	private double player_height;
	private double player_weight;
	private String player_team;
	private String player_specialty;
	private String player_prefession;
	private String player_face;
	public Integer getPlayer_id() {
		return player_id;
	}
	public void setPlayer_id(Integer player_id) {
		this.player_id = player_id;
	}
	public String getPlayer_name() {
		return player_name;
	}
	public void setPlayer_name(String player_name) {
		this.player_name = player_name;
	}
	public String getPlayer_country() {
		return player_country;
	}
	public void setPlayer_country(String player_country) {
		this.player_country = player_country;
	}
	
	public double getPlayer_height() {
		return player_height;
	}
	public void setPlayer_height(double player_height) {
		this.player_height = player_height;
	}
	public double getPlayer_weight() {
		return player_weight;
	}
	public void setPlayer_weight(double player_weight) {
		this.player_weight = player_weight;
	}
	public String getPlayer_team() {
		return player_team;
	}
	public void setPlayer_team(String player_team) {
		this.player_team = player_team;
	}
	public String getPlayer_specialty() {
		return player_specialty;
	}
	public void setPlayer_specialty(String player_specialty) {
		this.player_specialty = player_specialty;
	}
	public String getPlayer_prefession() {
		return player_prefession;
	}
	public void setPlayer_prefession(String player_prefession) {
		this.player_prefession = player_prefession;
	}
	public String getPlayer_face() {
		return player_face;
	}
	public void setPlayer_face(String player_face) {
		this.player_face = player_face;
	}
	
}
